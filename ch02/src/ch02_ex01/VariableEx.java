package ch02_ex01;
//0522완
public class VariableEx {				//변수 예제

	public static void main(String[] args) {
		int value = 10;					//10을 변수 value의 초기값으로 저장
		int result = value + 10;		//연산의 결과 값을 변수 result의 초기값으로 저장
		
		System.out.println("result->"+result);			//변수 result 값을 읽고 콘솔에 출력
		System.out.println("value->"+value);
	}

}
